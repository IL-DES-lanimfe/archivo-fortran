      F2K(II)=FK(II)*(XD2K(II)-XBB(II))/(XD2K(II)-XD1K(II))

C      WRITE(16,*)SNGL(RK(II)),SNGL(XAAA(II)),SNGL(XBBB(II))

       F12K(II)=F1K(II)+F2K(II)

       FTK(II)=F1K(II)*EXP(-XD1K(II)*TT)+F2K(II)*EXP(-XD2K(II)*TT)

c       WRITE(16,*)SNGL(RK(II)),SNGL(FTK(II))

